package model.bean;

/**
 *
 * @author Do Tien Hung
 * MO stands for Mobile Originated
 * Tin nhan duoc tao hoac tra loi tu nguoi su dung
 */
public class VTC_MO {
    int id=0;
    String txid = "";
    String cusid = "";
    String cuspwd = "";
    String mobileno = "";
    String msg = "";
    int serviceid = 0;
    
    public VTC_MO () {
    }
    
    public VTC_MO(int id, String txid, String cusid, String cuspwd, String mobileno, String msg){
        this.id = id;
        this.cusid = cusid;
        this.txid = txid;
        this.cuspwd = cuspwd;
        this.mobileno = mobileno;
        this.msg = msg;
    }

    public int getServiceid() {
        return serviceid;
    }

    public void setServiceid(int serviceid) {
        this.serviceid = serviceid;
    }
    
    

    public String getCusid() {
        return cusid;
    }

    public void setCusid(String cusid) {
        this.cusid = cusid;
    }

    public String getCuspwd() {
        return cuspwd;
    }

    public void setCuspwd(String cuspwd) {
        this.cuspwd = cuspwd;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMobileno() {
        return mobileno;
    }

    public void setMobileno(String mobileno) {
        this.mobileno = mobileno;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getTxid() {
        return txid;
    }

    public void setTxid(String txid) {
        this.txid = txid;
    }
    
    
}
