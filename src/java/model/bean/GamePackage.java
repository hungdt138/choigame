package model.bean;

import java.util.Date;

/**
 *
 * @author Nguyen Dinh Doan
 */
public class GamePackage {

    private int pId;
    private String name;
    private String nameNoSign;
    private int packagePrice;
    private String logoUrl;
    private String extInfo;
    private String details;
    private int isPublish;
    private Date createDate;
    private Date editDate;

    /**
     * Default Constructor
     */
    public GamePackage() {
    }

    /**
     * Constructor with package id, name, price, logo, info
     * @param pId ID of the package
     * @param name Name of the package
     * @param packagePrice Price of the package
     * @param logoUrl Link to image of the package
     * @param extInfo Informations of the package
     */
    public GamePackage(int pId, String name, int packagePrice, String logoUrl, String detais) {
        this.pId = pId;
        this.name = name;
        this.packagePrice = packagePrice;
        this.logoUrl = logoUrl;
        this.details = detais;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

    public String getExtInfo() {
        return extInfo;
    }

    public void setExtInfo(String extInfo) {
        this.extInfo = extInfo;
    }

    public String getLogoUrl() {
        return logoUrl;
    }

    public void setLogoUrl(String logoUrl) {
        this.logoUrl = logoUrl;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNameNoSign() {
        return nameNoSign;
    }

    public void setNameNoSign(String nameNoSign) {
        this.nameNoSign = nameNoSign;
    }

    public Date getEditDate() {
        return editDate;
    }

    public void setEditDate(Date editDate) {
        this.editDate = editDate;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }

    public int getIsPublish() {
        return isPublish;
    }

    public void setIsPublish(int isPublish) {
        this.isPublish = isPublish;
    }

    public int getpId() {
        return pId;
    }

    public void setpId(int pId) {
        this.pId = pId;
    }

    public int getPackagePrice() {
        return packagePrice;
    }

    public void setPackagePrice(int packagePrice) {
        this.packagePrice = packagePrice;
    }
}
